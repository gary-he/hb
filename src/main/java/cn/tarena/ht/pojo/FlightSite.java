package cn.tarena.ht.pojo;

public class FlightSite {
	private Integer sId;
	private Integer sFlightPId;
	private String sType;
	private Integer sRate;
	private Integer sNum;
	public Integer getsId() {
		return sId;
	}
	public void setsId(Integer sId) {
		this.sId = sId;
	}
	public Integer getFlightPId() {
		return sFlightPId;
	}
	public void setFlightPId(Integer flightPId) {
		this.sFlightPId = flightPId;
	}
	public String getsType() {
		return sType;
	}
	public void setsType(String sType) {
		this.sType = sType;
	}
	public Integer getsRate() {
		return sRate;
	}
	public void setsRate(Integer sRate) {
		this.sRate = sRate;
	}
	public Integer getsNum() {
		return sNum;
	}
	public void setsNum(Integer sNum) {
		this.sNum = sNum;
	}
	
	

}
